#include "ft_printf.h"

int	convert_to_percent(int n)
{
	char	c;
	int		i;

	c = '%';
	i = 0;
	while (i != n / 2)
	{
		ft_putchar_fd(c, 1);
		i++;
	}
	return (n / 2);
}
